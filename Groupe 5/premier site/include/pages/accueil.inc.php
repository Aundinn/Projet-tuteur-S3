<p>L’association PR2L est une association d’intérêt général ouverte à
	tous qui a pour objectifs :
</p>
<p> - D’appeler et d’aider à la collecte de documents et archives concernant l’histoire ouvrière, des entreprises
	et des associations d’économie sociale et des syndicats professionnels
	en partenariat avec les institutions et organismes intéressés ;<br/>
	 - De promouvoir un programme de sauvegarde et de valorisation de ces
	documents et archives (guides, numérisation partagée de corpus
	documentaires ou des enquêtes) en liaison avec les Ministères, les
	chercheurs et les archivistes professionnels de la conservation
	(archivistes, bibliothécaires, documentalistes….) ; <br/>
	- De conduire des projets fédérateurs (colloques, rencontres, séminaires) à l’échelle
	régionale, nationale et internationale ; <br/>
	- De contribuer et participer à la diffusion documentaire et pédagogique des ressources collectées
	disponibles, en utilisant les outils informatiques appropriés.
</p>
