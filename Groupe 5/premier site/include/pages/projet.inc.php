<br/><p>
	<strong><strong>Présentation du projet de notre association</strong></strong>
</p>
<p>
	<strong>Le Projet de l’association PR2L vise à rassembler toute
		l’information existant en France et au delà </strong><strong>sur les
		archives des mouvements coopératifs et mutualistes</strong>
</p>
<p></p>
<p>
	<strong>1-</strong>Le Projet aura comme <strong>axes de travail </strong>:
</p>
<ul>
	<li>D’établir une cartographie nationale détaillée des fonds à partir
		d’un catalogue informatisé ;</li>
	<li>De mettre à la disposition des chercheurs des sources jusque là
		difficiles à exploiter, à partir de la numérisation et de la mise en
		ligne d’un nombre croissant d’archives ;</li>
	<li>De découvrir, de collecter et de mettre en valeur des nouveaux
		fonds d’archives ;</li>
	<li>De susciter, de participer à l’organisation de manifestations en
		direction du public (journées d’étude, conférences, colloques,
		expositions….) visant à faire connaître et reconnaître le rôle des
		mouvements coopératifs et mutualistes dans l’histoire extrêmement
		riche et trop peu connue du mouvement social.</li>
</ul>
<p>
	<strong>Pour réaliser les actions décrites ci-dessus, le Pôle s’est
		engagé à utiliser les technologies numériques les plus avancées y
		compris en créant un portail internet (<a href="http://www.pr2l.fr">http://www.pr2l.fr</a>);
		portail actuellement en reconstruction.
	</strong>
</p>
<p>
	<strong>2-L'action du pôle revêt plusieurs aspects :</strong>
</p>
<p>
	- appeler et aider à la <strong>collecte des documents et archives </strong>concernant
	l’histoire ouvrière, des entreprises et des associations d’économie
	sociale et des syndicats professionnels en partenariat avec les
	organismes intéressés ;
</p>
<p>
	- promouvoir un <strong>programme de sauvegarde et de valorisation de
		ces documents et archives </strong>(guides, numérisation partagée de
	corpus documentaires ou des enquêtes) en liaison avec les ministères,
	les chercheurs et les professionnels de la conservation (archivistes,
	bibliothécaires, documentalistes…);
</p>
<p>
	- conduire des <strong>projets fédérateurs</strong> (colloques,
	rencontres, séminaires) à l’échelle régionale, nationale et
	internationale ;
</p>
<p>- contribuer et participer à la diffusion documentaire et pédagogique
	des ressources exixtantes et celles découvertes…</p>
