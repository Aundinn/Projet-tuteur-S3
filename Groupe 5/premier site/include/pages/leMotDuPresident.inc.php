<div id="motDuPresident">
	<p>
		<strong>2013 a été une année décisive pour le Pôle.</strong>
	</p>
	<p>
		En effet, lors de notre dernière Assemblée Générale, l'association de
		préfiguration est devenue « <strong>Pôle International de Ressources
			de Limoges et du Limousin pour l'histoire du monde du travail et de
			l'économie sociale » (PR2L).</strong>
	</p>
	<p>
		Un partenariat a été constitué avec l<strong>a Fondation du Crédit
			Coopératif</strong> pour la réalisation de<strong> l'inventaire des
			archives de la Coopération et de la Mutualité</strong>.
	</p>
	<p>
		Le troisième événement marquant fût l'organisation d'une journée
		d'étude au Palais du Luxembourg en avril 2013, sur<strong> la
			valorisation du patrimoine et les perspectives d'avenir de l'économie
			sociale et solidaire en France et en Europe. </strong>Cet événement
		fera l'objet d'une publication aux éditions PULIM au deuxième semestre
		2014.
	</p>
	<p>
		Notre association est également allée à la rencontre des directrices
		et directeurs <strong>des centres d'archives publics du Limousin</strong>
		pour leur présenter<strong> la charte de travail en réseau</strong>.
		Ainsi, la Mairie de Limoges a-t-elle ratifié la charte en décembre
		2013.
	</p>
	<p>Pour plus de précisions sur nos activités, vous trouverez en pièces
		jointes un document de présentation de l'association avec le bilan des
		activités 2013 et un descriptif des actions prévues en 2014.</p>
	<p>Nous profitons de ce courrier pour vous annoncer que la prochaine
		Assemblée Générale de PR2L aura lieu le samedi 12 avril 2014 .</p>
	<p>
		Pour poursuivre nos activités et atteindre les objectifs que notre
		association s'est fixés pour 2014-2015, notamment pérenniser l'emploi
		de chargée de développement, créé en 2012, nous nous appuyons sur
		votre soutien moral et financier. Ce soutien donne crédibilité à notre
		association auprès des élus et des collectivités territoriales qui
		nous subventionnent et qui nous subventionneront à l'avenir. Ces
		cotisations constituent l'une des sources de recettes et rien ne
		serait possible sans votre soutien. C'est pourquoi, nous nous
		adressons à vous pour faire partie de notre association et pour<strong>
			solliciter de vous le versement de votre cotisation 2014</strong>
		(voir pièce jointe dans la rubrique "adhérer à notre association").
		Par avance, nous vous remercions et nous vous saurons gré de votre
		participation active à la vie de l'association et au développement de
		notre réseau d'adhérents.
	</p>
	<p>
		<strong>Toutes informations concernant les archives de l'histoire
			sociale et de ses acteurs sont les bienvenues.</strong>
	</p>
	<p></p>
	<p>Pour PR2L, Le Président</p>
