	<h1>le Projet du pôle (orientations, objectifs, axes d'actions....) </h1>
	<p>
		Le Projet de l’association PR2L vise à rassembler toute l’information existant en France et au delà sur les archives des mouvements coopératifs et mutualistes.
	</p>


	<p>
		<ol>
			<li>Le Projet aura comme <em>axes de travail</em> :</li>
			<ul>
				<li>D’établir une cartographie nationale détaillée des fonds à partir d’un catalogue informatisé ;</li>
				<li>De mettre à la disposition des chercheurs des sources jusque là difficiles à exploiter, à partir de la numérisation et de la mise en ligne d’un nombre croissant d’archives ;</li>
				<li>De découvrir, de collecter et de mettre en valeur des nouveaux fonds d’archives ;</li>
				<li>De susciter, de participer à l’organisation de manifestations en direction du public (journées d’étude, conférences, colloques, expositions….) visant à faire connaître et reconnaître le rôle des mouvements coopératifs et mutualistes dans l’histoire extrêmement riche et trop peu connue du mouvement social.</li>
			</ul>
	</p>
	<p><em>
		Pour réaliser les actions décrites ci-dessus, le Pôle s’est engagé à utiliser les technologies numériques les plus avancées y compris en créant un portail internet (http://www.pr2l.fr); portail actuellement en reconstruction.</em></p>
	<p>
		<li><em>L'action du pôle revêt plusieurs aspects</em> :</li>
		<ul>
			<li>appeler et aider à la <em>collecte des documents et archives</em> concernant l’histoire ouvrière, des entreprises et des associations d’économie sociale et des syndicats professionnels en partenariat avec les organismes intéressés ;</li>
			<li>promouvoir un <em>programme de sauvegarde et de valorisation de ces documents et archives</em> (guides, numérisation partagée de corpus documentaires ou des enquêtes) en liaison avec les ministères, les chercheurs et les professionnels de la conservation (archivistes, bibliothécaires, documentalistes…);</li>
			<li>conduire des <em>projets fédérateurs</em> (colloques, rencontres, séminaires) à l’échelle régionale, nationale et internationale ;</li>
			<li>contribuer et participer à la diffusion documentaire et pédagogique des ressources exixtantes et celles découvertes…</li>
		</ul>
		</ol>
