<article id="IHSCGT">
	<h1>L'Institut d'Histoire Sociale CGT Limoges</h1>
	<img src="./image/ihscgt.jpg" id="logoCRESS" class="logoPartenaires">
	<p>L’Institut CGT d’histoire sociale est une association créée en 1982 avec le soutien de la Confédération Générale du Travail. L’Institut mobilise ses atouts et ses compétences pour <em>donner à connaître aux salariés l’histoire sociale</em> et singulièrement la longue expérience de la CGT. Son action est diversifiée. En premier lieu, <em>l’Institut se consacre à la sauvegarde, au classement et à la valorisation des archives de la Confédération</em>. Cela le conduit à <em>ouvrir les archives à l’étude et à la recherche ainsi qu’à organiser des conférences, séminaires et colloques</em> seul ou en coopération avec des universitaires et des chercheurs.</p>
</article>

