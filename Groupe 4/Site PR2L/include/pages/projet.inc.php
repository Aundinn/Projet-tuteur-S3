	<h1>Présentation de l'association PR2L</h1>

	<img src="./image/presPR2L.jpg" id='imagPres'>
	<p>L’association PR2L est une association d’intérêt général ouverte à tous qui a pour objectifs :
		<ul>
			<li>D’appeler et d’aider à <em>la collecte de documents et archives concernant l’histoire ouvrière, des entreprises et des associations d’économie sociale et des syndicats professionnels</em> en partenariat avec les institutions et organismes intéressés ;</li>

			<li>De <em>promouvoir un programme de sauvegarde et de valorisation de ces documents et archives</em> (guides, numérisation partagée de corpus documentaires ou des enquêtes) en liaison avec les Ministères, les chercheurs et les archivistes professionnels de la conservation (archivistes, bibliothécaires, documentalistes….) ;</li>

			<li>De <em>conduire des projets fédérateurs</em> (colloques, rencontres, séminaires) à l’échelle régionale, nationale et internationale ;</li>

			<li>De <em>contribuer et participer à la diffusion documentaire et pédagogique des ressources collectées disponibles</em>, en utilisant les outils informatiques appropriés.</li>
		</ul>
	</p>
	<h1>le Projet du pôle (orientations, objectifs, axes d'actions....) </h1>
	<p>
		Le Projet de l’association PR2L vise à rassembler toute l’information existant en France et au delà sur les archives des mouvements coopératifs et mutualistes.
	</p>


	<p>
		<ol>
			<li>Le Projet aura comme axes de travail :</li>
			<ul>
				<li>D’établir une cartographie nationale détaillée des fonds à partir d’un catalogue informatisé ;</li>
				<li>De mettre à la disposition des chercheurs des sources jusque là difficiles à exploiter, à partir de la numérisation et de la mise en ligne d’un nombre croissant d’archives ;</li>
				<li>De découvrir, de collecter et de mettre en valeur des nouveaux fonds d’archives ;</li>
				<li>De susciter, de participer à l’organisation de manifestations en direction du public (journées d’étude, conférences, colloques, expositions….) visant à faire connaître et reconnaître le rôle des mouvements coopératifs et mutualistes dans l’histoire extrêmement riche et trop peu connue du mouvement social.</li>
			</ul>
	</p>
	<p>
		Pour réaliser les actions décrites ci-dessus, le Pôle s’est engagé à utiliser les technologies numériques les plus avancées y compris en créant un portail internet (http://www.pr2l.fr); portail actuellement en reconstruction.</p>
	<p>
		<li>L'action du pôle revêt plusieurs aspects :</li>

		- appeler et aider à la collecte des documents et archives concernant l’histoire ouvrière, des entreprises et des associations d’économie sociale et des syndicats professionnels en partenariat avec les organismes intéressés ; - promouvoir un programme de sauvegarde et de valorisation de ces documents et archives (guides, numérisation partagée de corpus documentaires ou des enquêtes) en liaison avec les ministères, les chercheurs et les professionnels de la conservation (archivistes, bibliothécaires, documentalistes…); - conduire des projets fédérateurs (colloques, rencontres, séminaires) à l’échelle régionale, nationale et internationale ; - contribuer et participer à la diffusion documentaire et pédagogique des ressources exixtantes et celles découvertes…
		</ol>
	</p>
